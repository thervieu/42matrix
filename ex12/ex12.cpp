#include "../incs/Matrix.hpp"

int main(void) {
	ft::Matrix<float> m1({
		{1., 0., 0.},
		{0., 1., 0.},
		{0., 0., 1.}
		});
	m1.print("m1");
    m1.inverse().print("inverse");
	// [[1, 0, 0],
    //  [0, 1, 0],
    //  [0, 0, 1]]

	m1 = ft::Matrix<float>({
		{2., 0., 0.},
		{0., 2., 0.},
		{0., 0., 2.}
		});
	m1.print("m1");
    m1.inverse().print("inverse");
	// [[0.5, 0,   0],
    //  [0,   0.5, 0],
    //  [0,   0,   0.5]]

	m1 = ft::Matrix<float>({
		{8., 5., -2.},
		{4., 7., 20.},
		{7., 6.,  1.}
		});
	m1.print("m1");
    m1.inverse().print("inverse");
	// [[ 0.649425287,   0.097701149,  -0.655172414],
    //  [-0.781609195,  -0.126436782,   0.965517241],
    //  [ 0.143678161,   0.074712644,  -0.206896552]]
}
